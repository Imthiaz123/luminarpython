
def add(num1,num2):
    addn=num1+num2
    return addn
res=add(10,20)
print("add:",res)

def sub(num1,num2):
    subn=num1-num2
    return subn
res=sub(100,40)
print("sub:",res)

def mul(num1,num2):
    muln=num1*num2
    return muln
res=mul(10,20)
print("mul:",res)

def div(num1,num2):
    divn=num1/num2
    return divn
res=div(50,20)
print("div:",res)

def mod(num1,num2):
    modul=num1%num2
    return modul
res=mod(50,20)
print("mod:",res)

def pow(num1):
    power=num1**2
    return power
res=pow(10)
print("power:",res)

def fldiv(num1,num2):
    fdiv=num1//num2
    return fdiv
res=fldiv(50,30)
print("float div:",res)

